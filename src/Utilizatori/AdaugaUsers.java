    private void jbtn_adauga_userActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_adauga_userActionPerformed
        // TODO add your handling code here:
        
        //verificare campuri completate
        if(jtf_utilizator.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Va rugam sa completati un nume de utilizator !");
        }
        else
           if(jtf_nume.getText().isEmpty()){
               JOptionPane.showMessageDialog(null, "Va rugam sa completati numele dumneavoastra !");
           }
        else
            if(jpf_parola.getText().isEmpty()){
               JOptionPane.showMessageDialog(null, "Va rugam sa completati parola asociata contului, cu care trebuie sa va logati in aplicatie !");
            }
        else
            if(jtf_prenume.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Va rugam sa completati prenumele dumneavoastra !");
            }
        else
                if(jtf_telefon.getText().isEmpty()){
                    JOptionPane.showMessageDialog(null, "Va rugam sa completati telefonul dumneavoastra !");
                }
        else
                    if(jta_adresa.getText().isEmpty()){
                        JOptionPane.showMessageDialog(null, "Va rugam sa completati adresa dumneavoastra !");
                    }
        else
                    if(jcb_functie.getSelectedIndex()==0){
                        JOptionPane.showMessageDialog(null, "Va rugam sa alegeti o functie pentru utilizatorul dumneavoastra !");
                    }
        else
                        if(jtf_cnp.getText().isEmpty()){
                            JOptionPane.showMessageDialog(null, "Va rugam sa introduce-ti cnp-ul dumneavoastra!");
                        }
        else
                        //verificare lungime cnp corespunzatoare
        if((jtf_cnp.getText().length() < 13) || (jtf_cnp.getText().length() > 13)){
            JOptionPane.showMessageDialog(null, "CNP-ul corect nu este valid, doarece nu respecta lungimea");
        }
        else
                        {
                            utilizator.setUser(jtf_utilizator.getText());
                            utilizator.setNume(jtf_nume.getText());
            try {
                utilizator.setParola(criptare.MD5(jpf_parola.getText()));
            } catch (NoSuchAlgorithmException ex) {
                Logger.getLogger(AdaugaUsers.class.getName()).log(Level.SEVERE, null, ex);
            } catch (UnsupportedEncodingException ex) {
                Logger.getLogger(AdaugaUsers.class.getName()).log(Level.SEVERE, null, ex);
            }
                            utilizator.setPrenume(jtf_prenume.getText());
                            utilizator.setTelefon(jtf_telefon.getText());
                            utilizator.setAdresa(jta_adresa.getText());
                            utilizator.setFunctie(jcb_functie.getSelectedIndex());
                            utilizator.setCnp(jtf_cnp.getText());
                            System.out.println(utilizator.Afisare_User());
                            
                            //apelam functia de inserare bd
                            InserareUsersBD();
                            setVisible(false);
                        }
    }//GEN-LAST:event_jbtn_adauga_userActionPerformed

