    private void InserareUsersBD(){
        String sql = "Insert into utilizatori(utilizator, parola, nume, prenume, telefon, adresa, functie, cnp) "
                    + "VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
            try {
                PreparedStatement ps = connect.prepareStatement(sql);
                ps.setString(1, utilizator.getUser());
                ps.setString(2, utilizator.getParola());
                ps.setString(3, utilizator.getNume());
                ps.setString(4, utilizator.getPrenume());
                ps.setString(5, utilizator.getTelefon());
                ps.setString(6, utilizator.getAdresa());
                ps.setInt(7, utilizator.getFunctie());
                ps.setString(8, utilizator.getCnp());
                ps.executeUpdate();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            } 
            try{
            rs.close();
            ps.close();
            System.out.println("Inchidere rs si ps!");
        }
        //exceptie inchidere bd
        catch(Exception eibd){
            JOptionPane.showMessageDialog(null, eibd);
        }
    }
    
    private void InserareFunctie(){
        try{
            String sql = "Select * from functie where denumire = 'Antrenor'";
            ps=connect.prepareStatement(sql);
            rs=ps.executeQuery();
            while(rs.next()){
                int id = rs.getInt("id");
                String denumire =rs.getString("denumire");
                jcb_functie.insertItemAt(denumire, id);
                
            }
        }
        catch(Exception exip){
            JOptionPane.showMessageDialog(null, exip);
        }
    }
